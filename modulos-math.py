#MATH - é um módulo que precisa ser importado;
#Módulo é um conjunto de funções empacotadas em um só 
import math
print(math.ceil(4.2))# esta função mostra o teto do numero; = 5
print(math.floor(4.2))# Mesma coisa que o round ou ceil, contudo o floor da o piso;  = 4
print(math.pow(4,2))# aqui é a potência de 4**2 = 16
print(math.sqrt(144))# Este modulo traz a raiz quadrada do numero = 12

