#No python uma string também é um index
#Ex. 
mensagem = '012ABCDEFG3456789'
print(mensagem[0]) # eSTE PRINT EXIBIRÁ O STRING 0 
print(mensagem[3]) # ESTEE PRINT VAI EXIBIR O INDEX 3 QUE REPRESENTA A LETRA 'A'

#FATIAMENTO 
print(mensagem[0:])#este print mostra a mensagem completa 012ABCDEFG3456789
print(mensagem[0:-1]) # Este print mostra até a penultima letra da mensagem 012ABCDEFG345678
print(mensagem[3: 5]) #Este print mostra do indice 3 ao 4, pois mostra sempre o anterior ao escolhido AB

